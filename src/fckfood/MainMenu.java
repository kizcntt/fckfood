    public void CapNhatTien(){
        if(gh.tbGiohang.getRowCount()!=0)
            btnThanhtoan.setEnabled(true);
        else
            btnThanhtoan.setEnabled(false);
        int tien = 0;
        for(int i = 0; i<gh.tbGiohang.getRowCount();i++){
         int gia = Integer.parseInt(gh.tbGiohang.getValueAt(i, 2).toString());
         int sl = Integer.parseInt(gh.tbGiohang.getValueAt(i, 3).toString());
         long thanhtien = gia*sl;
          tien += thanhtien;
          
        }
        SetTien(Integer.toUnsignedLong(tien));
    }
    
    
    public void SetTien(long tien){
    DecimalFormat formatter = (DecimalFormat) NumberFormat.getInstance(Locale.CANADA);
    DecimalFormatSymbols symbols = formatter.getDecimalFormatSymbols();
    symbols.setGroupingSeparator(' ');
    formatter.setDecimalFormatSymbols(symbols);
    tongtien = tien;
    lbTien.setText(formatter.format(tien));
    }
