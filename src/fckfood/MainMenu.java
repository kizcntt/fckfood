    public void updateTb() throws SQLException{
        tbMonchinh.setRowCount(0);
        tbMonphu.setRowCount(0);
        tbNuoc.setRowCount(0);
        MonChinh();
        MonPhu();
        Nuoc();
    }
    protected void MonChinh() throws SQLException{

        ResultSet rs = sp.getMon(1);
        ResultSetMetaData rsmd = rs.getMetaData();
        
        tbMonchinh.setColumnCount(rsmd.getColumnCount()-1);
        try { 
            while(rs.next()){ // nếu còn đọc tiếp được một dòng dữ liệu
            String rows[] = new String[5];
            rows[0] = rs.getString(1); // lấy dữ liệu tại cột số 1 (ứng với mã hàng)
            rows[1] = rs.getString(3);
            rows[2] = rs.getString(4);
            rows[3] = rs.getString(5);
            tbMonchinh.addRow(rows); // đưa dòng dữ liệu vào tableModel 
            //mỗi lần có sự thay đổi dữ liệu ở tableModel thì Jtable sẽ tự động update 
        }
        }
        catch (SQLException e) {
        } 
            
}
